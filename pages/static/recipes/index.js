import React from 'react'
import Head from 'next/head'
import axios from 'axios'
import Layout1 from '../../../components/layout1'
import styles from './StaticRecipes.module.css'
import Card from '../../../components/module/card/card'
import Router from 'next/router'

const StaticRecipes = ({ recipes }) => {

    const handleRecipeClick = (id) => {
        Router.push(`/static/recipes/${id}`)
    }

    return (
        <>
            <Head>
                <title>Recipedia | Static Recipes</title>
                <meta name="description" content="Generated by Recipedia" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <div className={`${styles.static_recipes}`}>
                <Layout1>
                    <h1>Static Recipes</h1>
                    <div className={`${styles.cards}`}>
                        {recipes && recipes.map((recipe, idx) => {
                            return (
                                <Card
                                    recipeName={recipe.title}
                                    photo={!recipe.photo ? 'https://fakeimg.pl/500x500/?text=Hello' : recipe.photo}
                                    key={idx}
                                    onClick={() => handleRecipeClick(recipe.id)}
                                />
                            )
                        })

                        }
                    </div>
                </Layout1>
            </div>
        </>
    )
}

export const getStaticProps = async () => {
    // const { data: RespData } = await axios.get("http://localhost:4000/v1/recipes")
    const response = await axios.get('http://localhost:4000/v1/recipes')
    const dataRecipes = response.data.data
    console.log(dataRecipes)
    return {
        props: {
            recipes: dataRecipes
        }
    }
}

export default StaticRecipes